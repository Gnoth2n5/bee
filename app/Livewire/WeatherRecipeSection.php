        if (!$this->weatherData) {
            Log::info('No weather data found for city: ' . $this->selectedCity);
        } else {
            Log::info('Weather data found: ' . $this->weatherData->temperature . '¬∞C, ' . $this->weatherData->humidity . '%');
        }
    }

