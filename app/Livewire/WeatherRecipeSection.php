        } else {
            // Random ch·ªçn t·ª´ c√°c th√†nh ph·ªë c√≥ d·ªØ li·ªáu th·ªùi ti·∫øt
            $randomCityCode = $citiesWithWeather[array_rand($citiesWithWeather)];
