    public function randomCity()
    {
        Log::info('randomCity called - user denied location permission');

        // L·∫•y danh s√°ch t·∫•t c·∫£ th√†nh ph·ªë c√≥ d·ªØ li·ªáu th·ªùi ti·∫øt
        $citiesWithWeather = WeatherData::select('city_code')
            ->distinct()
