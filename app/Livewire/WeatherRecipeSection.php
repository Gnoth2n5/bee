            ->pluck('city_code')
            ->toArray();

        if (empty($citiesWithWeather)) {
            // N·∫øu kh√¥ng c√≥ th√†nh ph·ªë n√†o c√≥ d·ªØ li·ªáu th·ªùi ti·∫øt, l·∫•y t·∫•t c·∫£ th√†nh ph·ªë
